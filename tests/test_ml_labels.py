def test_꼬리행_미래5일없음_NaN():
    # 6행짜리 데이터에서 T=0만 라벨 가능(T+5=행5 존재). T=1~5는 미래 5일 부족 → NaN.
    df = _ohlcv([
        (100, 101, 99, 100, 1000),
        (100, 103, 100, 102, 1000),
        (102, 105, 101, 104, 1000),
        (104, 107, 103, 106, 1000),
        (106, 109, 105, 108, 1000),
        (108, 111, 107, 110, 1000),
    ])
    labels = make_labels(df)
    assert not math.isnan(labels.iloc[0])
    for i in range(1, 6):
        assert math.isnan(labels.iloc[i])


def test_인덱스_보존_및_길이일치():
    df = _ohlcv([(100, 101, 99, 100, 1000)] * 8)
    labels = make_labels(df)
    assert len(labels) == len(df)
    assert list(labels.index) == list(df.index)
